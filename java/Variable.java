/**
 * 【一句话功能简述:】Define variables for all basic data types
 * @author apple
 * @create 2021/1/1
 * @since 2020.1.4
 */

public class Variable {
    public static void main(String arg[]){
     //1、字节型
     byte b = 100;
     System.out.println(b);
     //2、短整型
     short s = 245;
     System.out.println(s);
     //3、整型
     int i = 1024;
     System.out.println(i);
     //4、长整型
     long l = 65536;
     System.out.println(l);
     //5、单精度浮点型
     float f = 5.20f;
     System.out.println(f);
     //6、双精度浮点型
     double d = 100000000;
     System.out.println(d);
     //7、布尔型
     boolean b1 = true;
     System.out.println(b1);
     //8、字符型
     char c = '薇';
     System.out.println(c);
    }
}